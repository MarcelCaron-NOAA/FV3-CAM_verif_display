This page displays verification statistics from three experimental 3-km limited area model (LAM) configurations of the Finite-Volume Cubed Sphere (FV3) model run at EMC:
<br>
<br>
<b>(1) FV3LAM</b>: The control LAM configuration run over North America
<br>
<br>
<!--b>(2) FV3LAM-X</b>: An experimental version of the LAM covering North America
<br>
<br-->
<b>(2) FV3LAM-DA</b>: An experimental version of the LAM that includes a 6-h DA cycle coldstarted from a 6-h LAM forecast. Run over CONUS only.
<br>
<br>
<b>(3) FV3LAM-DA-X</b>: Same as LAM-DA, but run over North America 
<br>
<br>
<br>
All three FV3 LAM configurations run twice a day over the CONUS at 00/12Z and run out to 60 hours. Verification comparisons against the NAM Nest, HRRR, and the FV3-based GFS are provided.
